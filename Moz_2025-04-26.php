        <h3>26 april 2025:</h3>
        <h2>Symfonie nr. 4 in D KV 19 en het klarinetconcert in A-groot KV 622</h2>
        <p>
            De tweede aflevering van
            <i>Mozart op Zaterdag</i>
            vindt plaats op zaterdagochtend 26 april. We werken dan aan Mozarts Symfonie nr. 4 in D KV 19 en het klarinetconcert in A-groot KV 622. De vierde symfonie is een kort jeugdwerk. Hij schreef het stuk in 1765 in Londen op 9-jarige leeftijd tijdens de Europese tour van de familie Mozart. De symfonie bestaat uit drie charmante delen, een Allegro, een Andante en een Presto, en duurt ongeveer 10 minuten. We spelen het zonder herhalingen.
        </p>
        <p>
            Het klarinetconcert is een van Mozarts laatste werken, voltooid in oktober 1791, slechts twee maanden voor zijn dood. Het is geschreven voor de klarinettist Anton Stadler, die ook de klarinetpartij speelde bij de première op 16 oktober 1791 in Praag. Het is een van de bekendste werken voor klarinet. Het klarinetconcert bestaat uit drie delen, een Allegro, een Adagio en een Rondo - Allegro. Het duurt ongeveer 30 minuten.
        </p>
